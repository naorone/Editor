    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        
        JFileChooser fileChooser = new JFileChooser();
        int seleccion = fileChooser.showOpenDialog(fileChooser);
        if (seleccion == JFileChooser.APPROVE_OPTION)
        {
            FileReader f = null;
            try {
                int cont= 0;
                ficheroAbierto = fileChooser.getSelectedFile();
                // Aquí debemos abrir el fichero para escritura
                // y salvar nuestros datos.
                
                jLabel1.setText(ficheroAbierto.getName());
                String cadena= "";
                f = new FileReader(ficheroAbierto);
                BufferedReader b = new BufferedReader(f);
                
                while((cadena = b.readLine())!=null)
                {
                    jTextArea1.append(cadena+"\n");     
                } 
                
                b.close();
                
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Editor.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Editor.class.getName()).log(Level.SEVERE, null, ex);
           
            } finally {
                try {
                    f.close();
                } catch (IOException ex) {
                    Logger.getLogger(Editor.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
           
        }
       
       
        
    }//GEN-LAST:event_jMenuItem3ActionPerformed

