    private void jMenuItem5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem5ActionPerformed
        // TODO add your handling code here:
        
        
        
        
        
        try 
        {
            // TODO code application logic here
            
            if(ficheroAbierto!=null) //Si se abrió un fichero
            {
                String textoFichAbierto = jTextArea1.getText();
                BufferedWriter bw;
                bw = new BufferedWriter(new FileWriter(ficheroAbierto));
                bw.write(textoFichAbierto);
                bw.close();
                
            }
            else if(ficheroCreado!=null) //si se creó un fichero
            {
                
                String textoNuevoFich = jTextArea1.getText();
                BufferedWriter bw;
                bw = new BufferedWriter(new FileWriter(ficheroCreado));
                bw.write(textoNuevoFich);
                bw.close();
                
                   
                
            }else{
                
                
                JFileChooser fileChooser = new JFileChooser();
                int seleccion = fileChooser.showSaveDialog(fileChooser);
                if (seleccion == JFileChooser.APPROVE_OPTION)
                {
                    File guardarComo = new File(fileChooser.getCurrentDirectory().getAbsolutePath()+"/"+fileChooser.getSelectedFile().getName());
                    guardarComo.createNewFile();
                    
                    String textoNuevoFich = jTextArea1.getText();
                    BufferedWriter bw;
                    bw = new BufferedWriter(new FileWriter(guardarComo));
                    bw.write(textoNuevoFich);
                    bw.close();
                }
            }
            
            
                   
          
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Editor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Editor.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_jMenuItem5ActionPerformed

